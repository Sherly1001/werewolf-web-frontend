<template>
  <div class="chat">
    <div class="top-navbar">
      <NavBar header="chat-room" />
    </div>

    <div class="messages" id="messages" v-on:scroll.passive="loadMoreMess">
      <DiscordMessages
        v-for="mess in messagesRecv[channel_id]"
        :key="mess.message_id"
      >
        <DiscordMessage :author="mess.username" :avatar="mess.avatar_url">
          {{ mess.message }}
        </DiscordMessage>
      </DiscordMessages>
    </div>
  </div>
</template>

<script>
import {
  DiscordMessage,
  DiscordMessages,
} from "@discord-message-components/vue";
import NavBar from "./NavBar.vue";
export default {
  props: {
    info: Object,
    messagesRecv: Object,
  },
  data() {
    return {
      hasMore: true,
      channel_id: "1",
    };
  },
  components: {
    DiscordMessage,
    DiscordMessages,
    NavBar,
  },
  methods: {
    loadMoreMess() {
      let offset = this.messagesRecv[this.channel_id].length;
      let messages = document.getElementById("messages");
      if (messages.scrollTop == 0) {
        if (this.hasMore) {
          this.$socket.send(
            JSON.stringify({
              GetMsg: {
                channel_id: this.channel_id,
                offset: offset,
                limit: 20,
              },
            })
          );
        }
      }
    },
    sendChannelId() {
      this.emitter.emit("sendChannel", this.channel_id);
    },
  },
  mounted() {
    console.log("Chat Mounted");
    this.sendChannelId();
    let messages = document.getElementById("messages");
    setTimeout(() => {
      messages.scrollTop = messages.scrollHeight;
    }, 0);
  },
  watch: {
    messagesRecv: {
      handler: function(newVal, oldVal) {
        console.log(newVal, oldVal);
        // if (oldVal.length > 0) {
        //   if (newVal[0].message_id == oldVal[0].message_id) {
        //     this.hasMore = false;
        //   }
        // }
        let messages = document.getElementById("messages");
        if (
          newVal &&
          messages.scrollHeight - messages.scrollTop == messages.clientHeight
        ) {
          setTimeout(() => {
            messages.scrollTop = messages.scrollHeight;
          }, 0);
        }
      },
      deep: true,
    },
  },
};
</script>

